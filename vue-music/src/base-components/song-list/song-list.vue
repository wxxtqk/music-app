<template>
  <div class="song-list">
    <ul>
      <li class="item" v-for="song in songs">
        <div class="content">
          <h2 class="name">{{song.name}}</h2>
          <p class="desc">{{song | setDesc}}</p>
        </div>
      </li>
    </ul>
  </div>
</template>

<script>
export default {
  props: {
    songs: {
      type: Array,
      default: function () {
        return []
      }
    }
  },
  filters: {
    setDesc (song) {
      return `${song.singer}.${song.album}`
    }
  }
}
</script>

